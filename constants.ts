// --- PARSER DE DADOS (Lista Manual com Títulos) ---

const parseMoviesFromRaw = (raw: string): Movie[] => {
  // Regex atualizado para capturar também o ID do IMDB (tt...)
  const regex = /(?:(\d+)\.\s*)?(.+?)\s*((?:https:\/\/drive\.google\.com|https:\/\/playerflixapi\.com|https:\/\/superflixapi\.buzz)\S+)/g;
  const idRegex = /\/((?:tt\d+)|\d+)/; 

  const movies: Movie[] = [];
  const seenUrls = new Set<string>();

  let match;
  let autoIdCounter = 1;

  while ((match = regex.exec(raw)) !== null) {
    let id: number;
    
    if (match[1]) {
        id = parseInt(match[1]);
    } else {
        id = autoIdCounter++;
    }

    const title = match[2].trim();
    
    if (title.toLowerCase().includes('carregando') || title.length < 2) {
        continue;
    }

    let videoUrl = match[3];

    let imdbId: string | undefined = undefined;
    const idMatch = videoUrl.match(idRegex);
    if (idMatch) {
        imdbId = idMatch[1];
    }

    if (videoUrl.includes('drive.google.com')) {
        const viewIndex = videoUrl.indexOf('/view');
        if (viewIndex !== -1) {
            const afterView = videoUrl.substring(viewIndex + 5);
            if (afterView.length > 0 && !afterView.startsWith('?')) {
                videoUrl = videoUrl.substring(0, viewIndex + 5);
            }
        }
    }

    if (!seenUrls.has(videoUrl)) {
        seenUrls.add(videoUrl);
        const randomImage = ACTION_IMAGES[id % ACTION_IMAGES.length];
        
        movies.push({
          id,
          imdbId, 
          title,
          imageUrl: randomImage,
          videoUrl,
          rating: '...',
          year: 2025, 
          description: `Carregando informações...`,
          duration: `...`,
          genre: 'Filme'
        });
    }
  }
  
  return movies;
};

