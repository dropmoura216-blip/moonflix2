// Parser específico para Séries
const parseSeriesFromIds = (rawIds: string): Movie[] => {
  const ids = rawIds.split('\n').map(s => s.trim()).filter(s => s.length > 0);
  const uniqueIds = Array.from(new Set(ids)); // Remove duplicatas automaticamente

  return uniqueIds.map((id, index) => {
     return {
        id: `series-${id}`, // ID único interno
        imdbId: id, // ID usado pelo serviço do TMDB para buscar metadados
        title: 'Carregando Série...',
        imageUrl: SERIES_IMAGES[index % SERIES_IMAGES.length],
        videoUrl: `https://superflixapi.buzz/serie/${id}`, // URL OBRIGATÓRIA PARA SÉRIES
        rating: '-',
        year: 2024,
        description: 'Aguarde, carregando informações da série...',
        genre: 'Série',
        duration: '...',
        type: 'series'
     };
  });
};

