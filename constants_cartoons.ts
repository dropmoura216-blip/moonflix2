// Parser específico para Desenhos
const parseCartoonsFromIds = (rawIds: string): Movie[] => {
  const ids = rawIds.split('\n').map(s => s.trim()).filter(s => s.length > 0);
  const uniqueIds = Array.from(new Set(ids)); // Remove duplicatas automaticamente

  return uniqueIds.map((id, index) => {
     return {
        id: `cartoon-${id}`, // ID único interno
        imdbId: id, // ID usado pelo serviço do TMDB para buscar metadados
        title: 'Carregando Desenho...',
        imageUrl: CARTOON_IMAGES[index % CARTOON_IMAGES.length],
        videoUrl: `https://superflixapi.buzz/serie/${id}`, // URL OBRIGATÓRIA PARA DESENHOS (Mesma estrutura de série)
        rating: '-',
        year: 2024,
        description: 'Aguarde, carregando informações do desenho...',
        genre: 'Desenho',
        duration: '...',
        type: 'cartoon' // Usa 'cartoon' para distinguir no filtro, mas será tratado como 'tv' na API
     };
  });
};

